    /**
     * 提交post请求
     *
     * @param path
     * @return
     */
    public static String PostRequset(String path, String name, String number) {
        try {
            URL url = new URL(path);
            //打开连接
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("POST");
            String data = "name=" + URLEncoder.encode(name, "UTF-8") +
                    "&number=" + URLEncoder.encode(number, "UTF-8");
            OutputStream out = conn.getOutputStream();
            out.write(data.getBytes());
            out.flush();
            out.close();
            conn.connect();

            if (200 == conn.getResponseCode()) {
                //得到输入流
                InputStream is = conn.getInputStream();
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                byte[] buffer = new byte[1024];
                int len = 0;
                while (-1 != (len = is.read(buffer))) {
                    baos.write(buffer, 0, len);
                    baos.flush();
                }
                return baos.toString("utf-8");
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null;
    }

    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mName;
        private final String mNumber;

        UserLoginTask(String name, String number) {
            mName = name;
            mNumber = number;
        }

        @Override
        protected Boolean doInBackground(Void... params) {

            String result = PostRequset("http://position.c.zmit.cn/index.php/api/login", mName, mNumber);
            try {
                System.out.println("resul" + result);
                JSONObject json = null;
                try {
                    json = new JSONObject(result);
                } catch (Exception e) {
                    return false;
                }

                String errorCode = json.getString("errorCode");
                switch (errorCode) {
                    case "0":
                        SharedPreferences.Editor editor = loginStorage.edit();
                        editor.putString("name", mName);
                        editor.putString("number", mNumber);
                        editor.commit();
                        return true;
                    case "1":
                        return false;
                }
            } catch (JSONException e) {
                e.printStackTrace();
            }
            return false;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                LoginActivity.this.finish();

                Intent intent = new Intent(LoginActivity.this, MainActivity.class);
                intent.putExtra("name", mName);
                intent.putExtra("number", mNumber);
                startActivity(intent);
            } else {
//                mNumberView.setError(getString(R.string.error_incorrect_password));
//                mNumberView.requestFocus();
                Toast.makeText(LoginActivity.this, "登陆信息有误或网络出错", Toast.LENGTH_SHORT).show();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
